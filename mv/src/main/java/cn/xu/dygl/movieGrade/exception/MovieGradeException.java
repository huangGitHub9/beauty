package cn.xu.dygl.movieGrade.exception;

public class MovieGradeException extends Exception {

    public MovieGradeException() {
        super();
    }

    public MovieGradeException(String message) {
        super(message);
    }

    public MovieGradeException(String message, Throwable cause) {
        super(message, cause);
    }

    public MovieGradeException(Throwable cause) {
        super(cause);
    }
}
